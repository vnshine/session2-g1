package myobject;

public class NhomHang {

	private Integer ID;
	private String sTenNhomHang;
	private String sTenNhomHangEng;
	private String sGhiChu;
	
	public Integer getID() {
		return ID;
	}
	public void setID(Integer iD) {
		ID = iD;
	}
	public String getsTenNhomHang() {
		return sTenNhomHang;
	}
	public void setsTenNhomHang(String sTenNhomHang) {
		this.sTenNhomHang = sTenNhomHang;
	}
	public String getsTenNhomHangEng() {
		return sTenNhomHangEng;
	}
	public void setsTenNhomHangEng(String sTenNhomHangEng) {
		this.sTenNhomHangEng = sTenNhomHangEng;
	}
	public String getsGhiChu() {
		return sGhiChu;
	}
	public void setsGhiChu(String sGhiChu) {
		this.sGhiChu = sGhiChu;
	}
	
}
